# operasi dapat dilakukan dengan penyingkatan
# operasi di tambah dengan assignment

a = 5 # adalah assignment
print("nilai a = ",a)

a += 1 # adalah a = a + 1
print("nilai a += 1, nilai a menjadi : ",a)

a -= 2 # adalah a = a - 2
print("nilai a -= 2, nilai a menjadi : ",a)

a *= 5 # adalah a = a * 5
print("nilai a *= 5, nilai a menjadi : ",a)

a /= 4 # adalah a = a / 4
print("nilai a /= 4, nilai a menjadi : ",a)

b = 10
print("\nnilai b = ", b)

b %= 3
print("nilai b %= ", b)

b = 10
