# perulangan atau looping

# list
angka_list = [0,1,2,3,4]
for i in angka_list:
    print(f"i sekarang {i}")
print("akhir dari program")

# range
angka_range = range(5)
for i in angka_range:
    print(f"i sekarang {i}")
print("akhir dari program")

angka_range_kedua = range(1,10)
for i in angka_range_kedua:
    print(f"i sekarang {i}")
print("akhir dari program")

angka_range_str = range(1,10)
for i in angka_range_str:
    print("saya keren")
print("akhir dari program")

# string
data_str = "bang lutfi sangat ganteng sekali tapi tukang ngocok"
