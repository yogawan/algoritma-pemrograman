# while loop

# print akan berputar-putar terus karena selalu benar jika 10 > 5
angka = 5
while angka < 15:
    angka += 1
    print(f"angka sekarang {angka}")

# print akan berhenti jika 0 < 5
angka_kedua = 20
while angka_kedua < 30:
    angka_kedua += 1
    print(f"angka sekarang {angka_kedua}")

